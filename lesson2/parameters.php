<?
// Defining a function named greet that accepts a parameter $name
function greet($name)
{
    echo "Hello, $name!";
}

// Calling the greet function and passing the argument "Alice"
greet("Alice"); // Output: Hello, Alice!


// Defining a function named add that accepts 2 parameters, which are:$a and $b
function add($a, $b)
{
    return $a + $b;
}

$result = add(5, 3); // $result = 8
